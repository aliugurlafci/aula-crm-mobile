export const translations: Record<Lang, Record<TKey, string>> = { en, tr, de };

/** Server document / queue statuses we have translations for. */
const KNOWN_STATUSES = new Set([
  'open', 'draft', 'checkout', 'posted', 'void', 'pending', 'syncing', 'done', 'failed',
]);

/** A status key only if we translate it, else null (caller shows the raw value). */
export function statusKey(status: string): TKey | null {
  return KNOWN_STATUSES.has(status) ? (`status.${status}` as TKey) : null;
}

/** Translation key for an outbox row kind, or null for an unknown kind. */
const KNOWN_KINDS = new Set([
  'pos.checkout', 'cart.create', 'cart.checkout', 'return.create', 'return.post',
]);
export function queueKey(kind: string): TKey | null {
  return KNOWN_KINDS.has(kind) ? (`queue.${kind}` as TKey) : null;
}
